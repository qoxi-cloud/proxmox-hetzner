#!/bin/bash
# Proxmox ASCII Logo - каждая буква отдельно, x оранжевые

O=$'\033[38;5;208m'   # Оранжевый
G=$'\033[38;5;240m'   # Тёмно-серый (было 250)
N=$'\033[0m'          # Reset

echo "${G} _____                                              ${N}"
echo "${G}|  __ \                                             ${N}"
echo "${G}| |__) | _ __   ___  ${O}__  __${G}  _ __ ___    ___  ${O}__  __${N}"
echo "${G}|  ___/ | '__| / _ \ ${O}\ \/ /${G} | '_ \` _ \  / _ \ ${O}\ \/ /${N}"
echo "${G}| |     | |   | (_) |${O} >  <${G}  | | | | | || (_) |${O} >  <${N}"
echo "${G}|_|     |_|    \___/ ${O}/_/\_\\${G} |_| |_| |_| \___/ ${O}/_/\_\\${N}"
